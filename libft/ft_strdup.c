#include "libft.h"

char		*ft_strdup(const char *src)
{
	char	*str;
	size_t	len;
	int		i;

	len = ft_strlen(src);
	i = 0;
	str = (char*)malloc(sizeof(*str) * (len + 1));
	if (str == NULL)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
